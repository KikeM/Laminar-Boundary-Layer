module Norms
implicit none

contains

!========================================================================================!
! 										    L8
!========================================================================================!
function Norm_L8(x)
	real :: x(0:)
	real Norm_L8

	Norm_L8 = maxval(abs(x))

end function

!========================================================================================!
! 										    Lp
!========================================================================================!
function Norm_Lp(x, p)
	real    :: x(0:)
	integer :: p
	real    :: Norm_Lp

	Norm_Lp = sum(abs(x)**p)
	Norm_Lp = Norm_Lp ** (1.0/p)

end function

!========================================================================================!
! 										    L2
!========================================================================================!
function Norm_L2(x)
	real :: x(0:)
	real :: Norm_L2

	Norm_L2 = Norm_Lp(x, 2)

end function


!========================================================================================!
! 										End of module
!========================================================================================!

end module Norms
